// resolveSystemDefault is the house-style system prompt applied to generated images:
// the site's generation.image.system_prompt wins, else the theme's image.genai.system_prompt,
// else the theme description (so even a theme without an explicit prompt nudges the style).
func resolveSystemDefault(cfg *config.Config, meta render.ThemeMeta) string {
	if sp := strings.TrimSpace(cfg.Generation.Image.SystemPrompt); sp != "" {
		return sp
	}
	if sp := strings.TrimSpace(meta.Image.GenAI.SystemPrompt); sp != "" {
		return sp
	}
	return strings.TrimSpace(meta.Description)
}

// genScheme marks a reference whose "path" is an AI image-generation prompt:
// hero: "gen:a fox in snow" or, in content, ![alt](<gen:a fox in snow?aspect=16:9>).
const genScheme = "gen:"

// genOutDir is the site-root-relative directory generated images are published to.
// Identical prompts share one file here, so it sits at the root rather than being
// co-located per page, and the "**assets/**" routing rule carries it to a store.
const genOutDir = "assets/generated"

// genRef is a parsed gen: reference — the prompt plus inline tuning params.
type genRef struct {
	Prompt string
	Params map[string]string
}

// parseGenRef parses gen:<prompt>[?k=v&…]. A trailing '?' with no key=value is left
// as part of the prompt (so "gen:why is the sky blue?" keeps its question mark).
func parseGenRef(ref string) (genRef, bool) {
	ref = strings.TrimSpace(ref)
	if !strings.HasPrefix(ref, genScheme) {
		return genRef{}, false
	}
	rest := strings.TrimPrefix(ref, genScheme)
	prompt, params := rest, map[string]string{}
	if i := strings.LastIndexByte(rest, '?'); i >= 0 && strings.Contains(rest[i+1:], "=") {
		if q, err := url.ParseQuery(rest[i+1:]); err == nil {
			prompt = rest[:i]
			for k, v := range q {
				if len(v) > 0 {
					params[k] = v[0]
				}
			}
		}
	}
	prompt = strings.TrimSpace(prompt)
	if prompt == "" {
		return genRef{}, false
	}
	return genRef{Prompt: prompt, Params: params}, true
}

// resolveImageGen resolves the configured image generator to ready-to-use settings,
// or nil when generation is off or misconfigured (a config error degrades to off
// with a warning rather than failing the build).
func resolveImageGen(cfg *config.Config, log *clog.Logger) *generate.Settings {
	if !cfg.Generation.Image.Configured() {
		return nil
	}
	s, err := generate.Resolve(cfg.Generation.Image)
	if err != nil {
		log.Step("IMAGE", "", "warn", "image generation disabled: "+err.Error())
		return nil
	}
	return &s
}

// genJob is one distinct image to produce, keyed by its extension-less stem. The
// filename (with the extension chosen from the generated bytes) is filled in once the
// image is found in cache or generated, and is what URLs and the output emission use.
type genJob struct {
	req      generate.ImageRequest
	stem     string
	filename string
}

// genResolver maps gen: references to stable asset URLs and accumulates the distinct
// images the build needs, keyed by their deterministic stem (so a prompt reused
// across posts is generated once). It is inert when s is nil (generation off),
// leaving gen: refs to fall through unresolved.
type genResolver struct {
	s        *generate.Settings
	cacheDir string
	// systemDefault is the house-style system prompt applied to every image unless a
	// post overrides or suppresses it (site config > theme system_prompt > theme description).
	systemDefault string
	jobs          map[string]*genJob
}

func newGenResolver(s *generate.Settings, root, systemDefault string) *genResolver {
	gr := &genResolver{s: s, systemDefault: strings.TrimSpace(systemDefault), jobs: map[string]*genJob{}}
	if s != nil {
		dir := s.OutputDir
		if !filepath.IsAbs(dir) {
			dir = filepath.Join(root, filepath.FromSlash(dir))
		}
		gr.cacheDir = dir
	}
	return gr
}

func (gr *genResolver) active() bool { return gr != nil && gr.s != nil }

// register returns the job for a gen ref, creating it on first sight. The per-ref
// `systemprompt` param is resolved into the request's system prompt and dropped from the
// provider params, so it shapes the cache identity without being sent as a tuning param.
func (gr *genResolver) register(g genRef) *genJob {
	system, params := effectiveSystem(g.Params, gr.systemDefault)
	stem := gr.s.ImageStem(g.Prompt, system, params)
	j := gr.jobs[stem]
	if j == nil {
		j = &genJob{req: gr.s.Request(g.Prompt, system, params), stem: stem}
		gr.jobs[stem] = j
	}
	return j
}

// systemPromptParam is the reserved query key authors use to override or suppress the
// house-style system prompt for a single image.
const systemPromptParam = "systemprompt"

// effectiveSystem resolves the system prompt for one reference and returns it alongside
// a copy of params with the reserved systemprompt key removed. An explicit
// none/nil/off/empty value suppresses the default; any other value replaces it; absence
// inherits the default.
func effectiveSystem(params map[string]string, def string) (string, map[string]string) {
	raw, ok := params[systemPromptParam]
	if !ok {
		return def, params
	}
	clean := make(map[string]string, len(params))
	for k, v := range params {
		if k != systemPromptParam {
			clean[k] = v
		}
	}
	switch strings.ToLower(strings.TrimSpace(raw)) {
	case "", "none", "nil", "off":
		return "", clean
	default:
		return strings.TrimSpace(raw), clean
	}
}

// note registers a gen: reference for generation, ignoring everything else. Called in
// the collection pass so every image is known before ensure runs.
func (gr *genResolver) note(ref string) {
	if !gr.active() {
		return
	}
	if g, ok := parseGenRef(ref); ok {
		gr.register(g)
	}
}

// resolveURL maps a gen: ref to the page/page-absolute URLs of its image. ok is false
// for non-gen refs or when generation is unconfigured, so callers fall back to normal
// asset handling. An image not (yet) produced keeps a stable .png ref, behaving like
// any other missing asset.
func (gr *genResolver) resolveURL(ref, basePath, baseURL string, router *core.Router) (rel, abs string, ok bool) {
	if !gr.active() {
		return "", "", false
	}
	g, isGen := parseGenRef(ref)
	if !isGen {
		return "", "", false
	}
	j := gr.register(g)
	if j.filename == "" {
		// Not produced — uncached and not generated this run, or generation failed/errored
		// (bad key, expired plan, refusal). Resolve to nothing so we never emit a broken ref.
		return "", "", false
	}
	out := genOutDir + "/" + j.filename
	if u := router.AssetURL(out); u != "" {
		return u, u, true
	}
	return basePath + out, absURL(baseURL, out), true
}

// genImageRE matches a whole markdown image (incl. the closing paren), capturing the alt
// text and destination, so a gen: embed can be rewritten to its URL or dropped entirely.
var genImageRE = regexp.MustCompile(`!\[([^\]]*)\]\(\s*(?:<([^>]+)>|([^)\s]+))\s*\)`)

// rewriteGenRefs replaces gen: image embeds with the resolved image URL. It runs before
// rewriteAssetURLs, emitting absolute/root-relative URLs the latter leaves untouched. A
// gen prompt with spaces must use the <…> destination form. An embed whose image wasn't
// produced (uncached/failed) is dropped to its alt text, so the body never carries a broken
// <img>. Non-gen images are left untouched for the asset pass.
func rewriteGenRefs(body, basePath, baseURL string, router *core.Router, gr *genResolver) string {
	if !gr.active() {
		return body
	}
	return genImageRE.ReplaceAllStringFunc(body, func(m string) string {
		sub := genImageRE.FindStringSubmatch(m)
		dest := sub[2]
		if dest == "" {
			dest = sub[3]
		}
		if _, isGen := parseGenRef(dest); !isGen {
			return m // not a gen ref — leave it for rewriteAssetURLs
		}
		if rel, _, ok := gr.resolveURL(dest, basePath, baseURL, router); ok {
			return "![" + sub[1] + "](" + rel + ")"
		}
		return sub[1] // not produced → drop the embed, keep the caption text
	})
}

// genResult is one job's outcome from the parallel phase: the log line to emit and any
// fatal filesystem error. Generation runs concurrently; the log lines replay serially
// in deterministic order from these.
type genResult struct {
	detail  bool  // true → Detail (a quiet cache hit), false → Step
	logArgs []any // key/values after ("IMAGE", provider)
	fatal   error
}

// ensure produces every registered image into the cache, recording each job's resolved
// filename, so the subsequent render can reference the real file (with the right
// extension) and emission can publish it. A cache hit (committed or from an earlier
// run) is reused without an API call; a miss is generated only when generateAI is set,
// otherwise warned and skipped — the build never fails.
//
// Provider calls are independent and slow, so they run in a bounded worker pool
// (s.Concurrency); each writes only its own distinct cache files. The cached/
// generated/skip log lines replay serially after the barrier, in deterministic order.
func (gr *genResolver) ensure(generateAI bool, log *clog.Logger, now time.Time) error {
	if !gr.active() || len(gr.jobs) == 0 {
		return nil
	}
	s := gr.s
	stems := gr.sortedStems()

	// Built at most once, lazily (only a cache miss needs it), shared across workers
	// (the drivers are stateless over a concurrency-safe http.Client).
	var genOnce sync.Once
	var gen generate.ImageGenerator
	var genErr error
	ensureGen := func() (generate.ImageGenerator, error) {
		genOnce.Do(func() { gen, genErr = generate.New(*s) })
		return gen, genErr
	}

	results := make([]genResult, len(stems))
	sem := make(chan struct{}, max(1, s.Concurrency))
	var wg sync.WaitGroup
	for i, st := range stems {
		wg.Add(1)
		sem <- struct{}{}
		go func(i int, j *genJob) {
			defer wg.Done()
			defer func() { <-sem }()
			results[i] = gr.produce(j, generateAI, ensureGen, now)
		}(i, gr.jobs[st])
	}
	wg.Wait()

	// A single "generator unavailable" line stands in for the per-job skips below.
	if genErr != nil {
		log.Step("IMAGE", s.Provider, "warn", "generator unavailable: "+genErr.Error())
	}
	for _, r := range results {
		if r.fatal != nil {
			return r.fatal
		}
		if r.detail {
			log.Detail("IMAGE", s.Provider, r.logArgs...)
		} else {
			log.Step("IMAGE", s.Provider, r.logArgs...)
		}
	}
	return nil
}

// produce resolves one job to a cached file: a hit reuses the existing file; a miss
// with generateAI generates and writes the image (extension chosen from its bytes)
// plus a sidecar. It sets j.filename on success and does no output emission.
func (gr *genResolver) produce(j *genJob, generateAI bool, ensureGen func() (generate.ImageGenerator, error), now time.Time) genResult {
	if fn, n, ok := findCached(gr.cacheDir, j.stem); ok {
		j.filename = fn
		return genResult{detail: true, logArgs: []any{"cached", fn, "bytes", n}}
	}
	if !generateAI {
		return genResult{logArgs: []any{"skip", j.stem, "hint", "build --generate-ai to create it"}}
	}
	gen, err := ensureGen()
	if err != nil {
		return genResult{logArgs: []any{"skip", j.stem}} // the single unavailable warn is logged by ensure
	}
	res, err := gen.Generate(context.Background(), j.req)
	if err != nil {
		return genResult{logArgs: []any{"warn", fmt.Sprintf("generate %q failed: %v", j.stem, err)}}
	}
	data := res.Bytes
	if gr.s.TrimLetterbox {
		if trimmed, ok := trimLetterbox(data, aspectValue(j.req.Params)); ok {
			data = trimmed
		}
	}
	ext, mime := imageExt(data, res.MIME)
	filename := j.stem + ext
	imagePath := filepath.Join(gr.cacheDir, filename)
	if err := os.MkdirAll(gr.cacheDir, 0o755); err != nil {
		return genResult{fatal: err}
	}
	if err := os.WriteFile(imagePath, data, 0o644); err != nil {
		return genResult{fatal: err}
	}
	if err := generate.WriteSidecar(imagePath, generate.Sidecar{
		Prompt:        j.req.Prompt,
		System:        j.req.System,
		Provider:      gr.s.Provider,
		Model:         j.req.Model,
		Params:        j.req.Params,
		MIME:          mime,
		RevisedPrompt: res.RevisedPrompt,
		Generated:     now.Format(time.RFC3339),
	}); err != nil {
		return genResult{fatal: err}
	}
	j.filename = filename
	return genResult{logArgs: []any{"generated", filename, "model", j.req.Model, "bytes", len(data)}}
}

// publishGeneratedImages copies every resolved cache image into the output tree. It
// runs after the pages are rendered (output emission mutates shared build state, so it
// stays serial); generation has already happened in ensure.
func publishGeneratedImages(write func(string, []byte) error, gr *genResolver, log *clog.Logger) error {
	if !gr.active() {
		return nil
	}
	for _, st := range gr.sortedStems() {
		j := gr.jobs[st]
		if j.filename == "" {
			continue // never produced (skipped/failed) — nothing to publish
		}
		b, err := os.ReadFile(filepath.Join(gr.cacheDir, j.filename))
		if err != nil {
			log.Step("IMAGE", gr.s.Provider, "warn", "cached image vanished: "+j.filename)
			continue
		}
		if err := write(genOutDir+"/"+j.filename, b); err != nil {
			return err
		}
		log.Detail("IMAGE", gr.s.Provider, "publish", j.filename, "bytes", len(b))
	}
	return nil
}

func (gr *genResolver) sortedStems() []string {
	stems := make([]string, 0, len(gr.jobs))
	for st := range gr.jobs {
		stems = append(stems, st)
	}
	sort.Strings(stems)
	return stems
}

// findCached returns the cached image file for a stem (any extension, excluding the
// .json sidecar) and its size, if one exists.
func findCached(cacheDir, stem string) (string, int64, bool) {
	matches, _ := filepath.Glob(filepath.Join(cacheDir, stem+".*"))
	for _, m := range matches {
		if strings.HasSuffix(m, ".json") {
			continue
		}
		if fi, err := os.Stat(m); err == nil && !fi.IsDir() {
			return filepath.Base(m), fi.Size(), true
		}
	}
	return "", 0, false
}

// imageExt picks a file extension and canonical MIME from the image bytes. The
// provider's declared type is only a fallback — MiniMax, for one, mislabels JPEG as
// PNG — and a correct extension matters because publishers derive Content-Type from it.
func imageExt(b []byte, fallbackMIME string) (ext, mime string) {
	switch m := http.DetectContentType(b); {
	case strings.HasPrefix(m, "image/png"):
		return ".png", "image/png"
	case strings.HasPrefix(m, "image/jpeg"):
		return ".jpg", "image/jpeg"
	case strings.HasPrefix(m, "image/webp"):
		return ".webp", "image/webp"
	case strings.HasPrefix(m, "image/gif"):
		return ".gif", "image/gif"
	}
	switch fallbackMIME {
	case "image/jpeg":
		return ".jpg", "image/jpeg"
	case "image/webp":
		return ".webp", "image/webp"
	case "image/gif":
		return ".gif", "image/gif"
	default:
		return ".png", "image/png"
	}
}
